public class PointSample {
    private float x;
    private float y;
    
    public PointSample() {
        this.x = 0.0f;
        this.y = 0.0f;
    }
    
    public PointSample(float x, float y) {
        this.x = x;
        this.y = y;
    }
    
    public float getX() {
        // Method stub
        return this.x;
    }
    
    public float getY() {
        // Method stub
        return this.y;
    }
}
