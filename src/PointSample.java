import java.util.List;
import java.util.ArrayList;

public class PointSample {
    private int x;
    private int y;
    private double value;
    
    public PointSample() {
        this.x = 0;
        this.y = 0;
        this.value = 0.0;
    }
    
    public PointSample(int x, int y, double value) {
        this.x = x;
        this.y = y;
        this.value = value;
    }
}